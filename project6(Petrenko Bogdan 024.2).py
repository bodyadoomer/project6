import random
import time

# Створюємо список дротів
colors = ['червоний', 'помаранчевий', 'жовтий', 'зелений', 'голубий', 'синій', 'фіолетовий']

# Вибираємо випадковий колір зі списку
correct_color = random.choice(colors)

# Пропонуємо гравцю вгадати колір дроту
guess = input("Який колір дроту потрібно перерізати, щоб знешкодити бомбу? ")

# Таймер на 30 секунд
start_time = time.time()
while time.time() - start_time < 30:
    pass

# Перевіряємо, чи правильно вгадав гравець
if guess == correct_color:
    print("Правильно! Ви знешкодили бомбу.")
else:
    print(f"Неправильно. Потрібно було перерізати {correct_color} дріт.")
